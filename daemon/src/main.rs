mod process_info;

use anyhow::Result;

#[tokio::main]
async fn main() -> Result<()> {
    tracing_subscriber::fmt()
        .with_env_filter(
            tracing_subscriber::EnvFilter::from_default_env()
                .add_directive("filesnitchd=info".parse()?),
        )
        .init();

    tracing::info!("filesnitchd starting");
    Ok(())
}
